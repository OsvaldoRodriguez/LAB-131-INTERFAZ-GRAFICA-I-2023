    private void jButtonAdicionarAmbiente1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAdicionarAmbiente1ActionPerformed
        // TODO add your handling code here:
        Empleado emp = new Empleado();
        
        emp.setNombre(this.jTextFieldNombre.getText());
        emp.setApellido(this.jTextFieldApellido.getText());
        if(this.jRadioButtonFemenino.isSelected()){
            emp.setSexo(this.jRadioButtonFemenino.getText());
        }
        if(this.jRadioButtonMasculino.isSelected()){
            emp.setSexo(this.jRadioButtonMasculino.getText());
        }
        if(this.jRadioButtonOtros.isSelected()){
            emp.setSexo(this.jRadioButtonOtros.getText());
        }
        
        boolean flag = true;
        int edad = 0;
        
        try {
            edad = Integer.parseInt(this.jTextFieldEdad.getText());
            emp.setEdad(edad);
        } catch (Exception e) {
            flag = false;
            JOptionPane.showMessageDialog(null, "La edad debe ser un numero entero");
        }
        
        double sueldo = 0;
        
        try {
            sueldo = Double.parseDouble(this.jTextFieldSueldo.getText());
            emp.setSueldo(sueldo);
        } catch (Exception e) {
            flag = false;
            JOptionPane.showMessageDialog(null, "El  sueldo debe ser un numero decimal");
        }
        
        emp.setAmbiente(lista);
        if(flag){
            a.adicionar(emp);
            a.mostrar();
            lista = new ListaDobleAmbiente();
            
            this.jTextFieldNombre.setText("");
            this.jTextFieldApellido.setText("");
            this.jTextFieldEdad.setText("0");
            this.jTextFieldSueldo.setText("0.0");
        }
        
        
    }//GEN-LAST:event_jButtonAdicionarAmbiente1ActionPerformed

