    private void jButtonGuardarPilaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonGuardarPilaActionPerformed
        String nomArch = "Institucion.dat";
        // 
        inst.setNombre(this.jTextFieldNombreInst.getText());
        inst.setUbicaccion(this.jTextFieldInstitucionUbicacion.getText());
        inst.setP(a);
        
        try{
            ObjectOutputStream arch = null;
            if(new File(nomArch).exists())
                arch = new AddObjectOutputStream(new FileOutputStream(nomArch, true));
            else
                arch = new ObjectOutputStream(new FileOutputStream(nomArch, true));
            
            
            arch.writeObject(inst);
            a = new Pila(); 
            arch.close();   
            JOptionPane.showMessageDialog(null, "Institucion adicionada correctamente");
            this.jTextFieldInstitucionUbicacion.setText("");
            this.jTextFieldNombreInst.setText("");
          
        } catch (Exception e) {
               
            System.out.println("adicionar "  + e);
        }
        
        
        
        
        
        
    }//GEN-LAST:event_jButtonGuardarPilaActionPerformed
    
    public void mostrarTabla(){
        tabla.setNumRows(0);
            limpiarFormEmpleado();
            Pila aux = new Pila();
            while(!a.esVacia()){
                Empleado ee = (Empleado)a.eliminar();
                aux.adicionar((Object)ee);
                String datos[] = new String[5];
                datos[0] = ee.getNombre();
                datos[1] = ee.getApellido();
                datos[2] = ee.getSexo();
                datos[3] = ee.getEdad() + "";
                datos[4] = ee.getSueldo() + "";
                
                tabla.addRow(datos);
            }
            
            a.vaciar(aux);
            
    }
