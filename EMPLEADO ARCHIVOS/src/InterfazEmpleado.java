    public void mostrarTabla(){
        tabla.setNumRows(0);
            limpiarFormEmpleado();
            Pila aux = new Pila();
            while(!a.esVacia()){
                Empleado ee = (Empleado)a.eliminar();
                aux.adicionar((Object)ee);
                String datos[] = new String[5];
                datos[0] = ee.getNombre();
                datos[1] = ee.getApellido();
                datos[2] = ee.getSexo();
                datos[3] = ee.getEdad() + "";
                datos[4] = ee.getSueldo() + "";
                
                tabla.addRow(datos);
            }
            
            a.vaciar(aux);
            
    }
