    private void jButtonAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAdicionarActionPerformed
        // TODO add your handling code here:
        boolean okey = true;
            Empleado e = new Empleado();
            e.setNombre(this.jTextFieldNombre.getText());
            e.setApellido(this.jTextFieldApellido.getText());

            if(this.jRadioButtonFemenino.isSelected()){
                e.setSexo(this.jRadioButtonFemenino.getText().toString());
            }

            if(this.jRadioButtonMasculino.isSelected()){
                e.setSexo(this.jRadioButtonMasculino.getText().toString());
            }

            if(this.jRadioButtonOtros.isSelected()){
                e.setSexo(this.jRadioButtonOtros.getText().toString());
            }


            try {
                int edad = Integer.parseInt(this.jTextFieldEdad.getText());
                e.setEdad(edad);
            } catch (Exception e1) {
                JOptionPane.showMessageDialog(null, "Edad debe ser entero");
                okey = false;
            }

            try {
                double sueldo = Double.parseDouble(this.jTextFieldSueldo.getText());
                e.setSueldo(sueldo);
            } catch (Exception e1) {
                JOptionPane.showMessageDialog(null, "Sueldo debe ser decimal");
                okey = false;
            }


            if(okey){
                a.adicionar((Object)e);
                mostrarTabla();
                a.mostrar();
                JOptionPane.showMessageDialog(null, "Aniadido a la PIla");
                this.jProgressBarPila.setValue(this.jProgressBarPila.getValue() + 1);
                if(this.jProgressBarPila.getValue() == 5){
                    this.jButtonAdicionar.setEnabled(false);
                }

            }
        
    }//GEN-LAST:event_jButtonAdicionarActionPerformed

